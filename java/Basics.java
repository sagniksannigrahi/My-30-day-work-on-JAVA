public class Basics {
    public static void main(String[] args) {
        int age = 20;
        double price = 99.99;
        char grade = 'A';
        boolean javaFun = true;

        int sum = 10 + 20;
        int product = 10 * 5;

        boolean equal = (10 == 20);
        boolean greater = (10 > 5);

        System.out.println("Age: " + age);
        System.out.println("Price: " + price);
        System.out.println("Grade: " + grade);
        System.out.println("Java is fun: " + javaFun);
        System.out.println("Sum: " + sum);
        System.out.println("Product: " + product);
        System.out.println("Is 10 equal to 20? " + equal);
        System.out.println("Is 10 greater than 5? " + greater);
    }
}
